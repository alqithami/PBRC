
from __future__ import annotations

import argparse
from pathlib import Path
from typing import List

from .exp1_social_cascades import run as run_exp1
from .exp1b_example_run import run as run_exp1b
from .exp2_token_sufficiency import run as run_exp2
from .exp3_topology_token_flow import run as run_exp3
from .exp4_incomplete_router import run as run_exp4
from .exp1c_ablation_lambda import run as run_exp1c
from .exp5_cost_dos import run as run_exp5

def run_all(out_dir: Path, seed: int = 0) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    paths: List[Path] = []
    paths.append(Path(run_exp1(out_dir, seed=seed)))
    run_exp1b(out_dir, seed=seed)
    paths.append(Path(run_exp2(out_dir, seed=seed)))
    paths.append(Path(run_exp3(out_dir, seed=seed)))
    paths.append(Path(run_exp4(out_dir, seed=seed)))
    paths.append(Path(run_exp1c(out_dir, seed=seed)))
    paths.append(Path(run_exp5(out_dir, seed=seed)))

    report = out_dir / "REPORT.md"
    lines = [
        "# PBRC simulation reproducibility report\n",
        f"Generated by `python -m pbrc.experiments.run_all --out {out_dir} --seed {seed}`\n",
        "## Outputs\n",
    ]
    for p in paths:
        lines.append(f"- `{p.name}`\n")
    lines.append("\n## Figures\n")
    for fig in sorted(out_dir.glob("*.png")):
        lines.append(f"- `{fig.name}`\n")
    report.write_text("".join(lines), encoding="utf-8")

if __name__ == "__main__":
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", type=str, default="results/sim")
    ap.add_argument("--seed", type=int, default=0)
    args = ap.parse_args()
    run_all(Path(args.out), seed=args.seed)
